class BuildSolution:
    def __init__(self) -> None:
        pass

    def build(self, psf: str, pdb: str, salt: float, ofile: str, hmr: int, 
              padding: float = 18.0, pad_x_pos: float = None, pad_x_neg: float = None,
              pad_y_pos: float = None, pad_y_neg: float = None, pad_z_pos: float = None,
              pad_z_neg: float = None):
        
        # Determine specific axis padding or use global padding
        has_granular = 1 if any(p is not None for p in [pad_x_pos, pad_x_neg, pad_y_pos, pad_y_neg, pad_z_pos, pad_z_neg]) else 0
        px_pos = pad_x_pos if pad_x_pos is not None else padding
        px_neg = pad_x_neg if pad_x_neg is not None else padding
        py_pos = pad_y_pos if pad_y_pos is not None else padding
        py_neg = pad_y_neg if pad_y_neg is not None else padding
        pz_pos = pad_z_pos if pad_z_pos is not None else padding
        pz_neg = pad_z_neg if pad_z_neg is not None else padding

        f = open("PSFGenSol.tcl", "w")
        tclscript = """\
#! Generated by MSTBx PSFGenSol module
set psf %s 
set pdb %s 
set sol solvated
set output %s 
set salt %s 

mol new $psf type psf waitfor all 
mol addfile $pdb type pdb waitfor all 

set model [atomselect top "all"] 
set center [measure center $model]
set minmax [measure minmax $model]
set min [lindex $minmax 0]
set max [lindex $minmax 1]
set dimens [vecsub $max $min]

# STRICT CUBIC LOGIC - Using longest dimension of the molecule
set max_dim [expr {max([lindex $dimens 0],[lindex $dimens 1],[lindex $dimens 2])}]
set p %s
set boxlen [expr ($max_dim + 2 * $p) / 2 + 1]

# Define cubic box centered at molecule center
set boxmin [list [expr [lindex $center 0] - $boxlen] [expr [lindex $center 1] - $boxlen] [expr [lindex $center 2] - $boxlen]]
set boxmax [list [expr [lindex $center 0] + $boxlen] [expr [lindex $center 1] + $boxlen] [expr [lindex $center 2] + $boxlen]]

# Override with specific granular padding ONLY if provided by user
if {%s} {
    set minx [expr [lindex $min 0] - %s]
    set maxx [expr [lindex $max 0] + %s]
    set miny [expr [lindex $min 1] - %s]
    set maxy [expr [lindex $max 1] + %s]
    set minz [expr [lindex $min 2] - %s]
    set maxz [expr [lindex $max 2] + %s]
    set boxmin [list $minx $miny $minz]
    set boxmax [list $maxx $maxy $maxz]
}

# Solvate and Ionize
package require solvate
solvate $psf $pdb -minmax [list $boxmin $boxmax] -o $sol -s W 

mol delete all 
package require autoionize 
autoionize -psf $sol.psf -pdb $sol.pdb -cation SOD -anion CLA -sc $salt -o ionized -seg ION

# Recentering to 0,0,0
mol delete all 
mol new ionized.psf 
mol addfile ionized.pdb type pdb waitfor all
set all [atomselect top all]
set center [measure center $all]
$all move [transoffset [vecsub {0 0 0} $center]]
$all writepdb $output.pdb 
$all writepsf $output.psf

# Final PBC calculation from ACTUAL atoms (Strictly Square/Cubic)
set wat [atomselect top "all"]
set final_minmax [measure minmax $wat]
set f_min [lindex $final_minmax 0]
set f_max [lindex $final_minmax 1]
set f_vec [vecsub $f_max $f_min]

# Use the maximum extension to ensure a large enough cubic box for NAMD
set L [expr {max([lindex $f_vec 0],[lindex $f_vec 1],[lindex $f_vec 2])}]

set fout [open "step3_pbcsetup.str" w]
puts $fout "SET A = $L"
puts $fout "SET B = $L"
puts $fout "SET C = $L"
puts $fout "SET XCEN = 0"
puts $fout "SET YCEN = 0"
puts $fout "SET ZCEN = 0"
close $fout 

if {%s} {
    package require psfgen
    psfcontext reset
    readpsf $output.psf pdb $output.pdb
    hmassrepart
    writepsf $output.hmr.psf
    writepdb $output.hmr.pdb
}
quit
""" % (psf, pdb, ofile, salt, padding, has_granular, 
       px_neg, px_pos, py_neg, py_pos, pz_neg, pz_pos, hmr)
        f.write(tclscript)
        f.close()

class BuildSolutionSMD:
    def __init__(self) -> None:
        pass

    def build(self, psf, pdb, salt, ofile, hmr, atomsvec1, atomsvec2, extrapadz, padding=18.0):
        # SMD logic: Align to Z, Strict XY Square, Pad Z+ with extra space
        f = open("PSFGenSolSMD.tcl", "w")
        tclscript = """\
#! Generated by MSTBx PSFGenSolSMD module
set psf %s 
set pdb %s 
set sol solvated
set output %s 
set salt %s 

mol new $psf type psf waitfor all 
mol addfile $pdb type pdb waitfor all 

set all [atomselect top all]
set selanchor [atomselect top "%s"]
set selpull [atomselect top "%s"]

# Orienting to Z axis
set com_anchor [measure center $selanchor]
set com_pull [measure center $selpull]
set axis [vecsub $com_pull $com_anchor]
set M [transvecinv $axis]
$all move $M
set M [transaxis y -90]
$all move $M
$all writepdb orientedtoZ.pdb
mol delete all

# Recalculate box after orientation
mol new $psf type psf waitfor all
mol addfile orientedtoZ.pdb type pdb waitfor all
set all [atomselect top all]
set center [measure center $all]
set minmax [measure minmax $all]
set f_min [lindex $minmax 0]
set f_max [lindex $minmax 1]
set dimens [vecsub $f_max $f_min]

# STRICT XY SQUARE BASE LOGIC - Using max dimension for XY
set max_dim [expr {max([lindex $dimens 0],[lindex $dimens 1],[lindex $dimens 2])}]
set p %s
set boxlen [expr ($max_dim + 2 * $p) / 2 + 1]

# Base XY (Square)
set minx [expr [lindex $center 0] - $boxlen]
set maxx [expr [lindex $center 0] + $boxlen]
set miny [expr [lindex $center 1] - $boxlen]
set maxy [expr [lindex $center 1] + $boxlen]

# Z with Extra Padding
set p_extra [expr %s]
set minz [expr [lindex $center 2] - $boxlen]
set maxz [expr [lindex $center 2] + $boxlen + $p_extra]

set boxmin [list $minx $miny $minz]
set boxmax [list $maxx $maxy $maxz]

package require solvate
solvate $psf orientedtoZ.pdb -minmax [list $boxmin $boxmax] -o $sol -s W

mol delete all
package require autoionize
autoionize -psf $sol.psf -pdb $sol.pdb -cation SOD -anion CLA -sc $salt -o ionized -seg ION

# Final centering and ACCURATE PBC report
mol delete all 
mol new ionized.psf 
mol addfile ionized.pdb type pdb waitfor all
set all [atomselect top all]
set center [measure center $all]
$all move [transoffset [vecsub {0 0 0} $center]]

# Measure actual coordinates for final PBC (Square in XY)
set wat [atomselect top "all"]
set final_minmax [measure minmax $wat]
set f_min [lindex $final_minmax 0]
set f_max [lindex $final_minmax 1]
set f_vec [vecsub $f_max $f_min]

set L_xy [expr {max([lindex $f_vec 0],[lindex $f_vec 1])}]
set L_z [lindex $f_vec 2]

set fout [open "step3_pbcsetup.str" w]
puts $fout "SET A = $L_xy"
puts $fout "SET B = $L_xy"
puts $fout "SET C = $L_z"
puts $fout "SET XCEN = 0"
puts $fout "SET YCEN = 0"
puts $fout "SET ZCEN = 0"
close $fout

$all writepdb $output.pdb
$all writepsf $output.psf

if {%s} {
    package require psfgen
    psfcontext reset
    readpsf $output.psf pdb $output.pdb
    hmassrepart
    writepsf $output.hmr.psf
    writepdb $output.hmr.pdb
}
quit
""" % (psf, pdb, ofile, salt, atomsvec1, atomsvec2, padding, extrapadz, hmr)
        f.write(tclscript)
        f.close()
