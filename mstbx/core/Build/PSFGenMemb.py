import os
import warnings
import time 
from ..Utils.Utils import UnixMessage 
from datetime import datetime
warnings.filterwarnings("ignore")

class BuildMembrane:
    def __init__(self) -> None:
        pass

    def build(self, psf: str, pdb: str, salt: float, ofile: str, hmr: int, peptide: int, 
              moveZ: float, padding: float = 25.0):
        """
        Builds a membrane system. 
        
        Padding evolution in Z-axis:
        - Original in-house scripts: 20.0 A
        - CHARMM-GUI standard: 22.5 A
        - Current MSTBx standard: 25.0 A
        
        Strict XY square box based on max dimensions of protein/lipids.
        """
        f = open("PSFGenMemb.tcl", "w")
        script = """\
#! Generated by MSTBx PSFGenMemb module
set psf %s 
set pdb %s 
set sol solvated
set output %s 
set salt %s 
set peptidemem %s
set padding %s

mol new $psf type psf waitfor all 
mol addfile $pdb type pdb waitfor all

if {$peptidemem} {
    set psf orient.psf 
    set pdb orient.pdb
    set headlipid [atomselect top {lipids and name "P*" and z > 0}]
    set headcom [measure center $headlipid]
    set zheadcom [lindex $headcom 2]
    set pep [atomselect top protein]
    set pepcom [measure center $pep]
    set zpepcom [lindex $pepcom 2]

    set extended %s
    set currentd [expr $zheadcom - $zpepcom]
    set z [expr $extended - $currentd]
    $pep moveby [list 0 0 $z]

    set all [atomselect top all]
    $all writepsf orient.psf
    $all writepdb orient.pdb
} 

mol delete all
mol new $psf 
mol addfile $pdb

# Calculate XY size based on lipids/protein - STRICT XY SQUARE LOGIC
set all [atomselect top "lipids and name P or protein"] 
set minmax [measure minmax $all]
set xylen [vecsub [lindex $minmax 1] [lindex $minmax 0]] 
set maxvec [expr {max([lindex $xylen 0],[lindex $xylen 1])}]
set vec [expr $maxvec/2 + 1]

# Z padding: Use user-defined padding (default 25.0 A)
set zlen [vecsub [lindex $minmax 1] [lindex $minmax 0]]
set zboxpad [expr ([lindex $zlen 2] + (2 * $padding))/2 + 1]

set boxmin [list [expr -$vec] [expr -$vec] [expr -$zboxpad]]
set boxmax [list [expr $vec] [expr $vec] [expr $zboxpad]]

package require solvate
solvate $psf $pdb -minmax [list $boxmin $boxmax] -o $sol -s W 

# Cleanup bad waters
mol delete all
set id [mol new $sol.psf type psf waitfor all]
mol addfile $sol.pdb type pdb waitfor all 
set badw1 [atomselect top "waters and same residue as within 3 of protein"]
set badw2 [atomselect top "waters and same residue as abs(z) < 20"]
$badw1 set beta 1
$badw2 set beta 1
set allbadwater [atomselect top "name OH2 and beta > 0"]
set seglistwater [$allbadwater get segid]
set reslistwater [$allbadwater get resid]

mol delete all
package require psfgen
resetpsf
readpsf $sol.psf
coordpdb $sol.pdb
foreach segid $seglistwater resid $reslistwater {
   	delatom $segid $resid
}
writepsf ${sol}_removeW.psf
writepdb ${sol}_removeW.pdb

package require autoionize 
autoionize -psf ${sol}_removeW.psf -pdb ${sol}_removeW.pdb -cation SOD -anion CLA -sc $salt -o ionized -seg ION

# Final centering and STRICTLY SQUARE PBC reporting
mol delete all 
mol new ionized.psf 
mol addfile ionized.pdb type pdb waitfor all
set all [atomselect top all]
set center [measure center $all]
$all move [transoffset [vecsub {0 0 0} $center]]

# Measure ACTUAL extension of atoms (primarily waters) for final PBC
set final_atoms [atomselect top "all"]
set final_minmax [measure minmax $final_atoms]
set f_min [lindex $final_minmax 0]
set f_max [lindex $final_minmax 1]
set f_vec [vecsub $f_max $f_min]

# Enforce Square XY in the final report
set AB [expr {max([lindex $f_vec 0],[lindex $f_vec 1])}]
set C [lindex $f_vec 2]

set fout [open "step3_pbcsetup.str" w]
puts $fout "SET A = $AB"
puts $fout "SET B = $AB"
puts $fout "SET C = $C"
puts $fout "SET XCEN = 0"
puts $fout "SET YCEN = 0"
puts $fout "SET ZCEN = 0"
close $fout 

$all writepdb $output.pdb 
$all writepsf $output.psf

if {%s} {
    package require psfgen
    psfcontext reset
    readpsf $output.psf pdb $output.pdb
    hmassrepart
    writepsf $output.hmr.psf
    writepdb $output.hmr.pdb
}
quit
""" % (psf, pdb, ofile, salt, peptide, padding, moveZ, hmr) 
        f.write(script)
        f.close()
